const test = require("node:test");
const assert = require("node:assert/strict");

const {
  extractCalendarTopicsFromText,
} = require("../services/program-topic-extractor/src/parsers/calendar-topics");

test("keeps standalone first topic and range rows in single-row thematic plan", () => {
  const text = `
Тематическое планирование курса (35 часов)
Название разделов, тем, занятий
Количество
часов
Тема1. Введение в курс.
Что такое краеведение
Тема 2. Малая родина
2.С чего начинается родина. Страницы истории поселка .
3.Поселок Печенга в годы ВОВ
4.Земляки-участники ВОВ.
5-6. Встреча за чашкой чая. Встреча с участниками ВОВ, тружениками тыла, вдовами ветеранов.
7. Создание колхозов. История и современность. Экскурсия в сельскую библиотеку.
8.Экскурсия в сельскую библиотеку.
9.Священные места в поселке.
10-11.Экспедиция по селу. Встреча со старожилами.
12-13.Защита проекта «Мой родной поселок»
Тема 3. Я и моя семья
14.Моя семья.
15.Типы и происхождение фамилий.
16.«Старая фотография рассказала».
17.Семейные реликвии. Памятные вещи.
18.Составление родословной семьи, рода
19.Экспедиция по селу. Сбор материалов.
20.Защита проекта. Моя родословная.
Тема 4.Фольклор моего края, поселка
21.Устное народное творчество поселка
22.Семейно-бытовые обряды
23.Национальные праздники
24.Национальные игры
25.Детскийфольклор: колыбельная песня…
Тема 5. Наша школа
26.История моей школы.
27.Символика родной школы.
28.Школьные традиции
29.Директора школы.
30.Знаменитые выпускники школы
31.Трудовой десант. Уборка возле обелиска.
32.Возложение цветов к обелиску.
Тема 6.Подведение итогов
33.Не исчезай моё село…
34.Устный журнал «С чего начинается Родина»
35. Итоговое занятие
1 час
`;

  const { topics } = extractCalendarTopicsFromText({
    text,
    documentPath: "/tmp/728301.docx",
    documentFormat: "docx",
  });

  assert.equal(topics.length, 32);
  assert.equal(topics.reduce((sum, topic) => sum + topic.hours_total, 0), 35);
  assert.equal(topics[0].topic_name, "Введение в курс. Что такое краеведение");

  const rangeTopics = topics.filter((topic) => topic.raw_payload.source_range?.includes("-"));
  assert.deepEqual(rangeTopics.map((topic) => ({
    planNumber: topic.plan_number,
    name: topic.topic_name,
    total: topic.hours_total,
  })), [
    {
      planNumber: "5-6",
      name: "Встреча за чашкой чая. Встреча с участниками ВОВ, тружениками тыла, вдовами ветеранов.",
      total: 2,
    },
    {
      planNumber: "10-11",
      name: "Экспедиция по селу. Встреча со старожилами.",
      total: 2,
    },
    {
      planNumber: "12-13",
      name: 'Защита проекта "Мой родной поселок"',
      total: 2,
    },
  ]);
});
